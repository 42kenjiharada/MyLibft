/*
C言語には 標準の striteri 関数はない けど、
文字列の各文字に対して関数を適用する関数 を 自作 できる！

1. striteri の考え方
strmapi は新しい文字列を作成するが、striteri は元の文字列を直接変更する
各文字に f(index, char *) を適用
malloc は不要（元の文字列を直接変更するため）

6. strmapi の注意点
✅ C言語には strmapi はないので malloc を使って自作する！
✅ 関数ポインタ char (*f)(unsigned int, char) を適用！
✅ 新しいメモリを確保するので free() が必要！
✅ NULL チェックをして安全に処理！
❌ オリジナルの文字列は変更しない（新しい文字列を返す）

7. strmapi のまとめ
✅ strmapi は、各文字に関数を適用し、新しい文字列を作成する！
✅ 文字列を変更せず、新しい malloc メモリに結果を格納！
✅ free() しないとメモリリークの原因になる！
✅ 大文字変換・インデックス操作・文字変換などに活用可能！
*/

#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	i;

	if (s == NULL || f == NULL)
		return ;
	i = 0;
	while (s[i] != '\0')
	{
		f(i, &s[i]);
		i++;
	}
}
