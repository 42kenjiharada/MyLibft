/*
strncmp は 標準ライブラリ <string.h> に含まれる関数で、
2つの文字列を先頭から最大 n 文字まで比較する関数 だよ。
✅ 最大 n 文字まで比較できる安全な関数
✅ 辞書順（ASCIIコード順）で大小を比較
✅ パスワードチェックや部分一致の確認に便利！
✅ NULL 終端 \0 も比較対象になる
❌ n を適切に設定しないと意図しない動作になることがある

n が 0 の場合
ループに入らずに i < n が偽となり、すぐに 0 が返されます。
→ これは標準の strncmp の仕様に一致します。

文字列の途中で差が現れる場合
差が見つかった時点で、その差（符号付きの整数）が返されます。
→ 標準の仕様と同じです。

片方が途中で終わる場合
終端文字 '\0' ともう一方の文字との差を返します。
→ 標準の動作です。
*/
#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n && s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			return ((unsigned char) s1[i] - (unsigned char) s2[i]);
		}
		i++;
	}
	if (i < n)
	{
		return ((unsigned char) s1[i] - (unsigned char) s2[i]);
	}
	return (0);
}
