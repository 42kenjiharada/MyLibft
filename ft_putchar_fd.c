/*
C言語には 標準の putchar_fd 関数はない けど、
指定したファイルディスクリプタ（fd）に1文字を書き込む関数 を 自作 できる！

1. putchar_fd の考え方
標準の putchar() は stdout（標準出力）に書き込む
putchar_fd は fd（ファイルディスクリプタ）を指定して書き込む
write() システムコールを使う

5. putchar_fd の注意点
✅ write(fd, &c, 1); で fd に1文字書き込む
✅ stdout（標準出力）、stderr（標準エラー）、ファイルにも書き込める！
✅ エラー処理は open() などの関数側で対応するのがベスト！
❌ fd が無効な場合、何も表示されないので open() のエラーチェックが必要！

6. putchar_fd のまとめ
✅ putchar_fd(c, fd) は fd に c を書き込む！
✅ 標準出力 (1)、標準エラー (2)、ファイル (fd) に書き込める！
✅ write(fd, &c, 1); を使えば簡単に実装可能！
✅ putstr_fd() で文字列を出力する関数も作れる！
*/

#include "libft.h"

void	ft_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
}
