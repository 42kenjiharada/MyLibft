/*
C言語には 標準の putendl_fd 関数はない けど、
指定したファイルディスクリプタ（fd）に文字列を書き込み、その後に改行を追加する関数 を 自作 できる！

1. putendl_fd の考え方
puts() は stdout に文字列＋改行を出力
putendl_fd は fd（ファイルディスクリプタ）を指定して puts() と同じ動作をする
write() を使って s を書き込んだ後に \n を追加

5. putstr_fd の注意点
✅ write(fd, s, strlen(s)); で fd に文字列を書き込む
✅ stdout（標準出力）、stderr（標準エラー）、ファイルにも書き込める！
✅ NULL チェックを忘れると segfault（セグフォ）が発生する可能性！
✅ エラー処理は open() などの関数側で対応するのがベスト！
❌ fd が無効な場合、何も表示されないので open() のエラーチェックが必要！

6. putstr_fd のまとめ
✅ putstr_fd(s, fd) は fd に s を書き込む！
✅ 標準出力 (1)、標準エラー (2)、ファイル (fd) に書き込める！
✅ write(fd, s, strlen(s)); を使えば簡単に実装可能！
✅ putendl_fd() を作れば改行付きの出力もできる！
*/

#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	if (s != NULL)
	{
		write(fd, s, ft_strlen(s));
		write(fd, "\n", 1);
	}
}
